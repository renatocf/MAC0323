/* Na linguagem C, existem TIPOS específicos para a manipulação de 
 * dados:
 *  
 * TIPOS:
 * - int     (inteiros);
 * - float   (ponto flutuante);
 * - char    (caracteres);
 * - Tabelas de símbolos
 *
 * Um tipo é um conjunto de valores mais operações definifas sobre 
 * esses valores. Ex:
 *   int: podem ser somados/subtraídos/multiplicados/divididos/... 
 * Para nós, não importa qual a implementação do tipo na linguagem,
 * quando ela está sendo feita na linguagem. Porém, ao montar tabelas
 * de símbolos (usando hashes, listas ligadas, etc.) a implementação 
 * será importante (porque alguns são mais úteis que outros).
 *
 * TABELAS DE SÍMBOLOS:
 *  Armazenar conjuntos dinâmicos de dados. Algumas operações básicas 
 *  delas são: entrar dados, tirar dados, saber se um dado está ou não
 *  no local.
 *  Um exemplo de tabela de símbolos que utilizamos foi no EP5 de
 *  MAC0122, em que realizávamos a maioria destas ações.
 */
